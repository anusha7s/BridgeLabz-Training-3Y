package Assignment.One.LevelOne;

/*
 * Program 3: Convert the distance of 10.8 kilometers to miles.
 * Hint: 1 km = 1.6 miles
 */
class KmToMilesFixed {
    public static void main(String[] args) {
        double km = 10.8;
        double miles = km / 1.6;

        System.out.println("The distance " + km + " km in miles is " + miles);
    }
}

